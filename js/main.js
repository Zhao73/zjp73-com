/* ========== Language Switching ========== */
function setLanguage(lang) {
  document.documentElement.setAttribute('data-lang', lang);
  localStorage.setItem('zjp-lang', lang);

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.getAttribute('data-lang-btn') === lang);
    btn.classList.toggle('is-primary', btn.getAttribute('data-lang-btn') === lang);
  });
}

/* ========== Stars Background ========== */
function createStars() {
  const container = document.getElementById('stars-bg');
  const count = 120;
  const fragment = document.createDocumentFragment();

  for (let i = 0; i < count; i++) {
    const star = document.createElement('div');
    star.className = 'star';
    const size = Math.random() < 0.7 ? 2 : 4;
    star.style.cssText = `
      width: ${size}px;
      height: ${size}px;
      left: ${Math.random() * 100}%;
      top: ${Math.random() * 100}%;
      animation: twinkle ${2 + Math.random() * 4}s infinite ${Math.random() * 3}s;
      opacity: ${0.3 + Math.random() * 0.7};
    `;
    fragment.appendChild(star);
  }
  container.appendChild(fragment);
}

/* ========== Pixel Avatar with Mouse Tracking ========== */
function initAvatar() {
  const canvas = document.getElementById('avatar-canvas');
  if (!canvas) return;
  const ctx = canvas.getContext('2d');
  const W = 96;
  const H = 96;
  const P = 4; // pixel size

  // Eye direction: 0=center, -1=left, 1=right, up/down
  let eyeOffX = 0;
  let eyeOffY = 0;

  function drawPixel(x, y, color) {
    ctx.fillStyle = color;
    ctx.fillRect(x * P, y * P, P, P);
  }

  function drawAvatar() {
    ctx.clearRect(0, 0, W, H);

    // Hair (pink)
    const hair = '#ff6ec7';
    for (let x = 4; x <= 19; x++) drawPixel(x, 1, hair);
    for (let x = 3; x <= 20; x++) drawPixel(x, 2, hair);
    for (let x = 3; x <= 20; x++) drawPixel(x, 3, hair);
    for (let x = 2; x <= 21; x++) drawPixel(x, 4, hair);

    // Face (skin)
    const skin = '#ffcc88';
    for (let y = 5; y <= 16; y++) {
      const xStart = y < 7 ? 4 : 3;
      const xEnd = y < 7 ? 19 : 20;
      for (let x = xStart; x <= xEnd; x++) drawPixel(x, y, skin);
    }
    // Chin
    for (let x = 5; x <= 18; x++) drawPixel(x, 17, skin);
    for (let x = 7; x <= 16; x++) drawPixel(x, 18, skin);

    // Eyes (cyan) - follow mouse
    const eyeColor = '#00d4ff';
    const leftEyeBaseX = 7 + eyeOffX;
    const leftEyeBaseY = 9 + eyeOffY;
    const rightEyeBaseX = 15 + eyeOffX;
    const rightEyeBaseY = 9 + eyeOffY;

    drawPixel(leftEyeBaseX, leftEyeBaseY, eyeColor);
    drawPixel(leftEyeBaseX + 1, leftEyeBaseY, eyeColor);
    drawPixel(leftEyeBaseX, leftEyeBaseY + 1, eyeColor);
    drawPixel(leftEyeBaseX + 1, leftEyeBaseY + 1, eyeColor);

    drawPixel(rightEyeBaseX, rightEyeBaseY, eyeColor);
    drawPixel(rightEyeBaseX + 1, rightEyeBaseY, eyeColor);
    drawPixel(rightEyeBaseX, rightEyeBaseY + 1, eyeColor);
    drawPixel(rightEyeBaseX + 1, rightEyeBaseY + 1, eyeColor);

    // Eye whites / highlight
    drawPixel(leftEyeBaseX, leftEyeBaseY, '#ffffff');
    drawPixel(rightEyeBaseX, rightEyeBaseY, '#ffffff');

    // Eyebrows
    const browColor = '#cc6699';
    for (let x = 6; x <= 9; x++) drawPixel(x, 7, browColor);
    for (let x = 14; x <= 17; x++) drawPixel(x, 7, browColor);

    // Mouth (green smile)
    const mouth = '#39ff14';
    drawPixel(9, 14, mouth);
    for (let x = 10; x <= 13; x++) drawPixel(x, 15, mouth);
    drawPixel(14, 14, mouth);

    // Hair bangs (front)
    for (let x = 3; x <= 6; x++) drawPixel(x, 5, hair);
    for (let x = 17; x <= 20; x++) drawPixel(x, 5, hair);
    drawPixel(3, 6, hair);
    drawPixel(4, 6, hair);
    drawPixel(19, 6, hair);
    drawPixel(20, 6, hair);
  }

  // Mouse tracking
  document.addEventListener('mousemove', e => {
    const rect = canvas.getBoundingClientRect();
    const centerX = rect.left + rect.width / 2;
    const centerY = rect.top + rect.height / 2;
    const dx = e.clientX - centerX;
    const dy = e.clientY - centerY;

    // Clamp eye movement to -1, 0, 1
    eyeOffX = dx > 50 ? 1 : dx < -50 ? -1 : 0;
    eyeOffY = dy > 50 ? 1 : dy < -50 ? -1 : 0;

    drawAvatar();
  });

  drawAvatar();
}

/* ========== Scroll Animations ========== */
function initScrollAnimations() {
  const observer = new IntersectionObserver(
    entries => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          entry.target.classList.add('visible');
        }
      });
    },
    { threshold: 0.1 }
  );

  document.querySelectorAll('.nes-container, .app-card').forEach(el => {
    el.classList.add('fade-in');
    observer.observe(el);
  });
}

/* ========== Typing Effect ========== */
function typeText(element, text, speed) {
  element.textContent = '';
  let i = 0;
  const timer = setInterval(() => {
    if (i < text.length) {
      element.textContent += text[i];
      i++;
    } else {
      clearInterval(timer);
    }
  }, speed);
}

/* ========== Smooth Navigation ========== */
function initNavigation() {
  document.querySelectorAll('.nav-link').forEach(link => {
    link.addEventListener('click', e => {
      e.preventDefault();
      const target = document.querySelector(link.getAttribute('href'));
      if (target) {
        target.scrollIntoView({ behavior: 'smooth' });
      }
    });
  });
}

/* ========== Cursor Effect ========== */
function initCursorEffect() {
  if (typeof cursoreffects !== 'undefined') {
    new cursoreffects.fairyDustCursor({
      colors: ['#00d4ff', '#ff6ec7', '#39ff14', '#ffe66d'],
    });
  }
}

/* ========== Init ========== */
document.addEventListener('DOMContentLoaded', () => {
  // Stars
  createStars();

  // Language
  const savedLang = localStorage.getItem('zjp-lang') || 'en';
  setLanguage(savedLang);

  // Language buttons
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.addEventListener('click', () => {
      setLanguage(btn.getAttribute('data-lang-btn'));
    });
  });

  // Typing effect
  const subtitle = document.querySelector('.typing-text');
  if (subtitle) {
    const lang = localStorage.getItem('zjp-lang') || 'en';
    const text = translations[lang]['hero.subtitle'];
    typeText(subtitle, text, 60);
  }

  // Avatar with mouse tracking
  initAvatar();

  // Scroll animations
  initScrollAnimations();

  // Navigation
  initNavigation();

  // Cursor effect
  initCursorEffect();
});
